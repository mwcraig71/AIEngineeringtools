/**
 * Get sorted list of section names for a dropdown.
 */
function getWShapeList() {
  return Object.keys(W_SHAPES).sort((a, b) => {
    // Sort by depth descending, then weight descending
    const pa = a.match(/W(\d+)x(\d+)/);
    const pb = b.match(/W(\d+)x(\d+)/);
    if (!pa || !pb) return 0;
    const da = parseInt(pa[1]), wa = parseInt(pa[2]);
    const db = parseInt(pb[1]), wb = parseInt(pb[2]);
    if (da !== db) return db - da;
    return wb - wa;
  });
}

/**
 * Get section properties for a W-shape by name.
 */
function getWShapeProps(name) {
  return W_SHAPES[name] || null;
}
